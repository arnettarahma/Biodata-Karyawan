    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        if("".equals(nik.getText()) || "".equals(alamat.getText()) || "".equals(jabatan.getText()) ||
            "".equals(nama.getText()) || "".equals(email.getText())) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        }else{
            String JK = "";
            if(laki.isSelected()){
                JK = "L";
            }else{
                JK = "P";
            }
            String SQL = "UPDATE tb_karyawan SET "
            +"nama='"+nama.getText()+"', jenis='"+JK+"', "
            +"jabatan='"+jabatan.getText()+"', email='"+email.getText()+"', "
            +"alamat='"+alamat.getText()+"'"
            +"WHERE nik='"+nik.getText()+"'";
            int status = KoneksiDB.execute(SQL);
            if(status == 1){
                JOptionPane.showMessageDialog(this, "Data berhasil diupdate","Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            }else{
                JOptionPane.showMessageDialog(this, "Data gagal diupdate","Sukses", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_editActionPerformed

    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        int baris = tabel.getSelectedRow();
        if(baris != -1){
            nik.setText(tabel.getValueAt(baris, 0).toString());
            nama.setText((String)tabel.getValueAt(baris, 1).toString());
            String jk = tabel.getValueAt(baris, 2).toString();
            if(jk.equals("Laki - Laki"))
            laki.setSelected(true);
            else
            perem.setSelected(true);
            jabatan.setText(tabel.getValueAt(baris, 3).toString());
            email.setText(tabel.getValueAt(baris, 4).toString());
            alamat.setText(tabel.getValueAt(baris, 5).toString());
        }
    }//GEN-LAST:event_tabelMouseClicked

